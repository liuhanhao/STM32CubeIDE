/**
 * @file    dht11.h
 * @brief   DHT11 温湿度传感器驱动接口
 *          实现单总线协议时序，读取 40 位原始数据并校验，
 *          数值超出合法范围时标记无效。
 */

#ifndef DHT11_H
#define DHT11_H

#include <stdint.h>
#include "app_globals.h"       /* DHT11Data_t */
#include "stm32f1xx_hal.h"     /* GPIO_TypeDef, GPIO_PIN_x */

/* ------------------------------------------------------------------ */
/*  硬件引脚配置                                                        */
/* ------------------------------------------------------------------ */

/** DHT11 数据引脚所在 GPIO 端口（可在此处修改） */
#define DHT11_GPIO_PORT   GPIOA

/** DHT11 数据引脚编号（可在此处修改） */
#define DHT11_GPIO_PIN    GPIO_PIN_0

/* ------------------------------------------------------------------ */
/*  函数声明                                                            */
/* ------------------------------------------------------------------ */

/**
 * @brief  初始化 DHT11 数据引脚为推挽输出并拉高总线
 */
void DHT11_Init(void);

/**
 * @brief  执行一次 DHT11 单总线读取（含起始信号、等待响应、接收 40 位、校验）
 *         温度有效范围：-20~60 °C；湿度有效范围：0~100%。
 *         超出范围或校验失败时 out->valid = 0。
 * @param  out  指向输出结构体的指针，不可为 NULL
 * @return 1 = 读取成功且校验通过；0 = 超时或校验错误
 */
uint8_t DHT11_Read(DHT11Data_t *out);

#endif /* DHT11_H */
